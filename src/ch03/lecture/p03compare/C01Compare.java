package ch03.lecture.p03compare;

public class C01Compare {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a < b);
        System.out.println(a <= b);
    }
}
