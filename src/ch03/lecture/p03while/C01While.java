package ch03.lecture.p03while;

public class C01While {
    public static void main(String[] args) {
        System.out.println(1);
        int a = 0;
        while (a < 4) {
            System.out.println(2);
            System.out.println(3);
            a++;
        }
        System.out.println(4);
    }
}
